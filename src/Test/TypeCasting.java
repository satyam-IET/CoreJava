package Test;

public class TypeCasting {

	public static void main(String[] args) {
		// implicit Type Casting
		
		char a = 'a';
		System.out.println("value of a :" + a);
		int b = 'z';
		System.out.println("value of b :" + b);
		
		// explicit type casting
		
	   double num = 14.54;
	   int num1 = (int)num;
	   System.out.println(num1);
		

	}

}
